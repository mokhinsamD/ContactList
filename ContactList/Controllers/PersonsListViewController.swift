    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        personsList.count
    }


    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "contact", for: indexPath)
        var content = cell.defaultContentConfiguration()
        let contact = personsList[indexPath.row]
        content.text = "\(contact.name) \(contact.surname)"
        cell.contentConfiguration = content
        return cell
    }
    
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        guard let ditailsVC = segue.destination as? DitailsViewController else { return }
        guard let indexPath = tableView.indexPathForSelectedRow else { return }
        ditailsVC.name = personsList[indexPath.row].name
        ditailsVC.surname = personsList[indexPath.row].surname
        ditailsVC.phone = personsList[indexPath.row].phone
        ditailsVC.email = personsList[indexPath.row].email
    }


